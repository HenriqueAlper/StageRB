package technique;

public class Four {
    public static void main(String []args){
        // Lógica a = soma o anterior + 2
        int a = 9;
        // Lógica b = multiplica o anterior * 2
        int b = 128;
        // Lógica c = soma o anterior com um número em crescente a cada 2 unidades começando por 1
        int c = 49;
        // Lógica d = soma o anterior com um número em crescente a cada 8 unidades começando por 12
        int d = 100;
        // Lógica e = é o início da sequência de Fibonacci
        int e = 11;
        // Lógica f = Todos os números começam com a letra D.
        int f = 200;
        System.out.println("a) 1, 3, 5, 7, " + a );
        System.out.println("b) 2, 4, 8, 16, 32, 64, " + b );
        System.out.println("c) 0, 1, 4, 9, 16, 25, 36, " + c );
        System.out.println("d) 4, 16, 36, 64, " + d );
        System.out.println("e) 1, 1, 2, 3, 5, 8, " + e );
        System.out.println("f) 2,10, 12, 16, 17, 18, 19, " + f);

    }
}
